<h3>Keyboard is not working?</h3> 
<p>Make sure the following meta tag is exist immediately after head tag.If not exist add it.</p>
<code>< meta http-equiv="Content-Type" content="text/html; charset=utf-8" /></code><br/>
	
<br/><br/><p><strong>Not properly displaying arabic characters and layout in some web browser.</strong><br/>
Make sure the following meta tag is exist immediately after head tag.If not exist add it.</p>
<code>< meta http-equiv="X-UA-Compatible" content="IE=Edge, chrome=1"/></code>

